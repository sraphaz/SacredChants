/**
 * Single source for URL locale params consumed by the browser (init-theme-locale.js).
 * Contract file: src/i18n/locale-url-contract.json (also imported by src/i18n/strings.ts).
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const contractPath = join(root, 'src', 'i18n', 'locale-url-contract.json');
const contract = JSON.parse(readFileSync(contractPath, 'utf8'));
const { supportedCanonical, urlAliases } = contract;

const allowedRaw = [...supportedCanonical];
for (const alias of Object.keys(urlAliases)) {
  const a = alias.toLowerCase();
  if (!allowedRaw.includes(a)) allowedRaw.push(a);
}

/** Maps lowercase URL ?lang= value to canonical locale key (for data-locale / storage). */
const paramToCanonical = {};
for (const loc of supportedCanonical) {
  paramToCanonical[String(loc).toLowerCase()] = loc;
}
for (const [alias, canon] of Object.entries(urlAliases)) {
  paramToCanonical[String(alias).toLowerCase()] = canon;
}

const payload = { allowedRaw, paramToCanonical };
const out = `/* Generated by scripts/generate-locale-url-data.mjs — do not edit.
 * Source: src/i18n/locale-url-contract.json
 */
(function (g) {
  g.__SC_LOCALE_URL__ = ${JSON.stringify(payload)};
})(typeof globalThis !== 'undefined' ? globalThis : this);
`;

const outPath = join(root, 'public', 'scripts', 'sc-locale-url-data.js');
writeFileSync(outPath, out, 'utf8');
console.log('Wrote', outPath);
