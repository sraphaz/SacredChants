const files = [
  'shanti-mantra.json',
  'ganapati-mantra.json',
  'om-nama-shivaya.json',
  'karpura-gauram.json',
  'twameva-mata.json',
  'gayatri.json',
  'shivopasana-mantra.json',
  'hanuman-chalisa.json',
];

for (const name of files) {
  const slug = name.replace(/\.json$/, '');
  const hi = HI[slug];
  if (!hi) {
    console.warn('No HI copy for', slug);
    continue;
  }
  const path = join(dir, name);
  const raw = readFileSync(path, 'utf8');
  const data = JSON.parse(raw);
  let changed = false;
  if (data.description && !data.description.hi) {
    data.description.hi = hi.description;
    changed = true;
  }
  if (data.about && !data.about.hi) {
    data.about.hi = hi.about;
    changed = true;
  }
  if (changed) {
    writeFileSync(path, JSON.stringify(data, null, 2) + '\n', 'utf8');
    console.log('Updated', name);
  } else {
    console.log('Skip (already has hi)', name);
  }
}
